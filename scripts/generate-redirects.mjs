// scripts/generate-redirects.mjs
// Patch #3: generates public/_redirects with 301s mapping
//   /store/<character>-style-<letter>  →  /store/<character>?option=<letter>
//
// Fetches character-variant slugs from Sanity (live source of truth, no hardcoded list).
// Safe to re-run: if public/_redirects exists and lacks our sentinel comment,
// the script refuses to overwrite — protects any hand-written rules.

import fs from 'node:fs';
import path from 'node:path';
import { createClient } from '@sanity/client';

const SENTINEL = '# AUTO-GENERATED by scripts/generate-redirects.mjs';
const REDIRECTS_PATH = path.resolve('public/_redirects');

const client = createClient({
  projectId: 'bqb4w421',
  dataset: 'production',
  apiVersion: '2024-12-01',
  useCdn: false,
});

console.log('Fetching character variant slugs from Sanity...');
const slugs = await client.fetch(
  '*[_type == "product" && defined(slug.current) && slug.current match "*-style-?"].slug.current'
);

const characterVariants = slugs
  .filter((s) => /-style-[a-j]$/.test(s) && !s.startsWith('personalised-'))
  .sort();

if (characterVariants.length === 0) {
  console.error('✗ No character variants found in Sanity. Aborting.');
  process.exit(1);
}

console.log(`  Found ${characterVariants.length} character variants.`);

// Build redirect lines.
// Format: `from  to  status` (whitespace-separated; Netlify is tolerant).
// Using two-space separation for readability.
const lines = characterVariants.map((slug) => {
  const character = slug.replace(/-style-[a-j]$/, '');
  const letter = slug.match(/-style-([a-j])$/)[1];
  return `/store/${slug}  /store/${character}?option=${letter}  301`;
});

const header = [
  SENTINEL,
  '# Source: scripts/generate-redirects.mjs',
  `# Generated: ${new Date().toISOString()}`,
  `# Entries: ${lines.length}`,
  '#',
  '# Maps legacy per-variant URLs to the new character-driven PDP with ?option=<letter>.',
  '# These URLs were retired in patch #4 (removal of src/pages/store/[slug].astro).',
  '# Re-generate by running: node scripts/generate-redirects.mjs',
  '',
];

const output = header.concat(lines).join('\n') + '\n';

// Safety check on existing file.
if (fs.existsSync(REDIRECTS_PATH)) {
  const existing = fs.readFileSync(REDIRECTS_PATH, 'utf8');
  if (!existing.startsWith(SENTINEL)) {
    console.error('✗ public/_redirects exists but was not generated by this script.');
    console.error('   It may contain hand-written rules. Refusing to overwrite.');
    console.error('   To proceed, move/back up the existing file, then re-run.');
    process.exit(1);
  }
  console.log('  Overwriting previously generated _redirects.');
}

fs.mkdirSync(path.dirname(REDIRECTS_PATH), { recursive: true });
fs.writeFileSync(REDIRECTS_PATH, output, 'utf8');

console.log(`✓ Wrote ${lines.length} redirects to public/_redirects`);
console.log(`  File size: ${output.length} bytes`);
console.log('\nSample (first 3):');
lines.slice(0, 3).forEach((l) => console.log(`  ${l}`));
console.log('\nSample (last 3):');
lines.slice(-3).forEach((l) => console.log(`  ${l}`));
